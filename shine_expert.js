function getWeekData(weekNo) {
  if (weekNo <= 0 || weekNo > 52) return null;
  const data = WEEKLY_DATA[weekNo];
  if (data) {
    data.avgOutdoorTemp = AVG_OUTDOOR_TEMP[weekNo] || null;
  }
  return data;
}

function getWeekNo(heatStart, targetDate) {
  const days = Math.floor((targetDate - heatStart) / (24 * 60 * 60 * 1000));
  return days < 0 ? 0 : Math.floor(days / 7) + 1;
}

function formatDate(date) {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  return `${y}-${m}-${d}`;
}

function analyze(heatStartStr, targetDateStr, sensor = {}) {
  const heatStart = new Date(heatStartStr);
  const targetDate = new Date(targetDateStr);
  const weekNo = getWeekNo(heatStart, targetDate);
  const daysFromStart = Math.floor((targetDate - heatStart) / (24 * 60 * 60 * 1000));
  const weekData = getWeekData(weekNo);
  
  return {
    date: formatDate(targetDate),
    weekNo,
    daysFromStart,
    weekData,
    environment: sensor,
    alerts: []
  };
}

